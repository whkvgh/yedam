package co.khj.bean;

public class UserBean {

	private String uid;	// 로그인 아이디
	private String upw;	// 로그인 비밀번호
	private String uname;	// 유저 닉네임
	private String ugender;	// 유저 성별
	private String ubirth;	// 유저 생일
	private String umail;	// 메일
	private String uphone;	// 연락처
	private String ureg;	// 가입일

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getUpw() {
		return upw;
	}

	public void setUpw(String upw) {
		this.upw = upw;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getUgender() {
		return ugender;
	}

	public void setUgender(String ugender) {
		this.ugender = ugender;
	}

	public String getUbirth() {
		return ubirth;
	}

	public void setUbirth(String ubirth) {
		this.ubirth = ubirth;
	}

	public String getUmail() {
		return umail;
	}

	public void setUmail(String umail) {
		this.umail = umail;
	}

	public String getUphone() {
		return uphone;
	}

	public void setUphone(String uphone) {
		this.uphone = uphone;
	}

	public String getUreg() {
		return ureg;
	}

	public void setUreg(String ureg) {
		this.ureg = ureg;
	}

}
